/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  MX_SPI1_Init();
  /* USER CODE BEGIN 2 */
  nrf24_init();
  nrf24_TXmode(TX_address, 10);

  HAL_ADC_Start_DMA(&hadc1, (uint32_t *) adc_buffer, 2);
  HAL_TIM_Base_Start(&htim3);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	// Reverse enable/disable
	if(HAL_GPIO_ReadPin(REVERSE_GPIO_Port, REVERSE_Pin) == GPIO_PIN_SET){
	  if(throttle <= 100){
	  direction = !direction;
	  HAL_GPIO_TogglePin(REVERSE_GPIO_Port, REVERSE_Pin);
	  }
	}

	throttle = adc_buffer[0];
	steering = adc_buffer[1];

	/* Scaling the steering value
	* Formula to scale a value from one range to another
	* output = output_start + ((output_end - output_start) / (input_end - input_start)) * (input - input_start)
	*/
	if(steering < 2047){
	  // Turn left
	  sideA = 0.1 + ((0.9 - 0.1) / (2046 - 0)) * (steering);
	  sideB = 1;
	}
	else if(steering > 2048){
	  // Turn right
	  sideA = 1;
	  sideB = 0.1 + ((0.9 - 0.1) / (4095 - 2049)) * (steering - 2049);
	}
	else if(steering == 2047 || steering == 2048){
	  // No steering
	  sideA = 1;
	  sideB = 1;
	}

	speed_left = throttle * sideA;
	speed_right = throttle * sideB;

	TX_data[0] = (uint8_t) direction;
	// Note: 16-bit values are sent as two 8-bit values with MSB stored in lower index than LSB.
	TX_data[1] = (speed_left >> 8) & 0xFF;
	TX_data[2] = (speed_left & 0xFF);
	TX_data[3] = (speed_right >> 8) & 0xFF;
	TX_data[4] = (speed_right & 0xFF);

	if(nrf24_Transmit(TX_data,5)){
		HAL_GPIO_TogglePin(TX_LED_GPIO_Port, TX_LED_Pin);
		HAL_Delay(100);
	}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

